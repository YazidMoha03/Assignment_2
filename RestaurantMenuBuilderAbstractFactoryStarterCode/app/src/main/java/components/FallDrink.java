package components;

/**
 * The FallDrink class represents a drink item for the Fall menu.
 * 
 * 
 * @author gouraya
 * @author Yazid Mohamed
 * 
 */
public class FallDrink implements Drink {

    /**
     * Returns the name of the Fall drink, which is "Margarita".
     * 
     * @return a String representing the Fall drink's name.
     */
    @Override
    public String toString() {
        return "Margarita\nDark Rum\nOrange Juice";
    }
}
