package components;

/**
 * The FallEntree class represents an entree item for the Fall menu.
 * 
 * @author gouraya
 * 
 */
public class FallEntree implements Entree {

    /**
     * Returns the name of the Fall entree, which is "Spinach and Artichoke Dip".
     * 
     * @return a String representing the Fall entree's name.
     */
    @Override
    public String toString() {
        return "Spinach and Artichoke Dip\nSesame Soy Tuna Tartare\nTuscan Bruschetta";
    }
}
